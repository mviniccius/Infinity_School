'''
    Crie um algoritmo que ao ser executado receba dois números de um usuário e imprima o resultado da soma entre esses dois números
    Crie um algoritmo que ao ser executado receba dois números de um usuário e imprima o resultado da subtração entre esses dois números
    Crie um algoritmo que ao ser executado receba dois números de um usuário e imprima o resultado da multiplicação entre esses dois números
    Crie um algoritmo que ao ser executado receba dois números de um usuário e imprima o resultado da divisão entre esses dois números
'''  
num1 = int(input("Digite o primeiro numero: "))
num2 = int(input("Digite o segundo numero: "))


print("A soma do", num1, "+", num2,"=", num1 + num2)
print("A subtracao do", num1, "-", num2,"=", num1 - num2)
print("A multiplicacao do", num1, "X", num2,"=", num1 * num2)
print("A divisao do", num1, "/", num2,"=", num1 / num2)
