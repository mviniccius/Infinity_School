nome_pessoa = "Jessé Gomes da Silva Filho" # <class 'str'>
apelido_pessoa = "Zeca Pagodinho"          # <class 'str'>
pessoa_brasileira = True                   # <class 'bool'>
pessoa_carioca = True                      # <class 'bool'>
idade_pessoa = 64                          # <class 'int'>
altura_pessoa = 1.68                       # <class 'float'>
peso_pessoa = 80.0                         # <class 'float'>

# Até então, você já conhece todos os esses tipos de dados.
# Além disso, você também sabe que os chamamos de "tipos de dados primitivos",
# pois esses tipos de dados vieram "primeiro".
#
# Ainda, você também sabe que é possível utilizar a função type(), dentro da
# função print() para exibir no terminal (tela) o tipo de dado atribuído à
# variável que estivermos manipulando. Veja abaixo:

# Exibindo o tipo de dado da variável 'nome_pessoa'
print(type(nome_pessoa))

# Exibindo o tipo de dado da variável 'apelido_pessoa'
print(type(apelido_pessoa))

# Exibindo o tipo de dado da variável 'pessoa_brasileira'
print(type(pessoa_brasileira))

# Exibindo o tipo de dado da variável 'pessoa_carioca'
print(type(pessoa_carioca))

# Exibindo o tipo de dado da variável 'idade_pessoa'
print(type(idade_pessoa))

# Exibindo o tipo de dado da variável 'altura_pessoa'
print(type(altura_pessoa))

# Exibindo o tipo de dado da variável 'peso_pessoa'
print(type(peso_pessoa))
